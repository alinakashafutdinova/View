<?php
// =====================================================================
// Роутер для встроенного веб-сервера PHP — удобно для локального
// запуска без Apache. Из корня проекта выполните:
//
//     php -S localhost:8000 -t www www/router.php
//
// и откройте http://localhost:8000
// =====================================================================

$uri = parse_url($_SERVER['REQUEST_URI'], PHP_URL_PATH);
$uri = urldecode($uri); // чтобы кириллица в адресе (/hello/Анна) читалась корректно

// Существующие статические файлы (styles.css и т.п.) отдаём как есть.
if ($uri !== '/' && file_exists(__DIR__ . $uri)) {
    return false;
}

// Иначе — эмулируем поведение .htaccess: путь кладём в route.
$_GET['route'] = ltrim($uri, '/');
require __DIR__ . '/index.php';
